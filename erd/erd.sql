-- create the inspections table
create table inspections (
	item_number varchar(20) not null,
	drawing_number varchar(20) not null,
	revision varchar(3) not null,
	date_measured date not null,
	inspection_id serial not null unique,
	disposition boolean,
	qc_full_inspect_interval integer,
	released_qty integer,
	completed_qty integer,
	inspector_id integer,
	operator_id integer,
	supplier_id integer,
	receiver_id integer,
	pur_id integer,
	job_id integer,
	primary key (item_number, drawing_number, revision, date_measured, inspection_id),
	foreign key (supplier_id) references suppliers(sup_id),
	foreign key (receiver_id) references receiver_numbers(rec_id),
	foreign key (pur_id) references purchase_orders(pur_id),
	foreign key (job_id) references job_orders(job_id),
	foreign key (inspector_id) references employees(emp_id),
	foreign key (operator_id) references employees(emp_id)
);

